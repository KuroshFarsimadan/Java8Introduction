

public class ForLoops {

	public static void main(String[] args) {
		System.out.println("******************");
		for (int i = 5; i >= 1; i--) {
			System.out.println(i);
		}
		System.out.println("******************");
		for (int i = 0; i < 10; i++) {
			System.out.println(i);
		}
		System.out.println("******************");
		for (int i = 0; i < 10; i += 2) {
			System.out.println(i);
		}
		System.out.println("******************");
		
		/*
		 * 
		 * for(; ;) { System.out.println(); } // Forever / Infinite loop
		 */

	}

}
